function [p1,p2,V]=MinMax(A)
    [M1,M2]=size(A);
    f1=ones(M1,1);
    A1=-(A+abs(min(min(A))))';
    b1=-ones(M2,1);
    xlow1=zeros(M1,1);
    xup1=ones(M1,1);
    %x1=linprog(f1,A1,b1,[],[],xlow1,xup1);
    [x1] = glpk (f1, A1, b1, xlow1, xup1);
    v1=1/sum(x1);
    f2=-ones(M2,1);
    A2=(A+abs(min(min(A))));
    b2=ones(M1,1);
    xlow2=zeros(M2,1);
    xup2=ones(M2,1);
    %x2=linprog(f2,A2,b2,[],[],xlow2,xup2);
    [x2] = glpk (f2, A2, b2, xlow2, xup2);
    v2=1/sum(x2);
    p1=v1*x1
    p2=v2*x2
    V=p1'*A*p2
end